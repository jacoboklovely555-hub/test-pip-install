#T2 ทำความสะอาดข้อความไทย-อังกฤษ (Text Cleaning)
#โจทย์: รับข้อความแล้วทำ 3 อย่าง
#ลบช่องว่างซ้ำ (space หลายตัวให้เหลือตัวเดียว)
#แปลงเป็น lowercase (เฉพาะอังกฤษ)
#ลบอิโมจิ/อักขระพิเศษ (เลือกทำด้วย regex)
#ผลลัพธ์: พิมพ์ข้อความก่อน-หลัง

import re

text = input("กรอกข้อความ:\n")

print("\nก่อนทำความสะอาด:")
print(text)

# ลบช่องว่างซ้ำ
text = re.sub(r'\s+', ' ', text)

# แปลงอังกฤษเป็น lowercase
text = text.lower()

# ลบ emoji / อักขระพิเศษ
text = re.sub(r'[^\w\sก-๙]', '', text)

print("\nหลังทำความสะอาด:")
print(text)


