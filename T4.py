#T4  ตรวจจับสแปมแบบง่าย (Rule-based Spam Filter)
#โจทย์: รับข้อความ 1 บรรทัด แล้วให้คะแนนสแปมจากคีย์เวิร์ด เช่นฟรี, ด่วน, คลิก, โปรโมชั่น, เงินคืน, รับทันที
#ถ้ามีคำต้องสงสัย 1 คำ = +2 คะแนน
#ถ้ามีลิงก์ http/https = +3 คะแนน
#ถ้ามีตัวอักษรซ้ำเกิน 4 ตัวติดกัน (เช่น "มากกกกก") = +2 คะแนนผลลัพธ์: สรุปคะแนนและระดับ (ปกติ/น่าสงสัย/สแปม)
 

import re
text = input("กรอกข้อความ:\n")

score = 0
keywords = ["สอบถาม", "เวลา", "สวัสดี", "เปิด", "พรุ่งนี้", "ธนาคาร"]

for word in keywords:
    if word in text:
        score += 2

if re.search(r'http[s]?://', text):
    score += 3

if re.search(r'(.)\1{4,}', text):
    score += 2

print("คะแนนสแปม:", score)

if score >= 6:
    print("ระดับ: สแปม")
elif score >= 3:
    print("ระดับ: น่าสงสัย")
else:
    print("ระดับ: ปกติ")


