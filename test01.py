def analyze_name(name):
   # พจนานุกรมค่าตัวเลขตามหลักนามศาสตร์
   char_weights = {
       'ก': 1, 'ด': 1, 'ถ': 1, 'ท': 1, 'ภ': 1, 'า': 1, 'ำ': 1, 'ุ': 1,
       'ข': 2, 'ช': 2, 'ง': 2, 'บ': 2, 'ป': 2, 'เ': 2, 'แ': 2, 'ี': 2, 'ู': 2,
       'ต': 3, 'ฑ': 3, 'ฒ': 3, 'ไ': 3,
       'ค': 4, 'ธ': 4, 'ร': 4, 'ว': 4, 'ญ': 4, 'ษ': 4, 'ะ': 4, 'ิ': 4, 'โ': 4, 'ั': 4,
       'ฉ': 5, 'ฌ': 5, 'ฎ': 5, 'ณ': 5, 'น': 5, 'ม': 5, 'ห': 5, 'ฮ': 5, 'ฬ': 5, 'ึ': 5,
       'จ': 6, 'ล': 6, 'ว': 6, 'อ': 6, 'ใ': 6,
       'ศ': 7, 'ส': 7, 'ื': 7, '๊': 7,
       'ย': 8, 'พ': 8, 'ฟ': 8, 'ผ': 8, 'ฝ': 8, '้': 8,
       'ฏ': 9, 'ฐ': 9, '์': 9, 'เ': 9, '๋': 9
   }
   # ลบช่องว่างออกเพื่อคำนวณเฉพาะตัวอักษร
   clean_name = name.replace(" ", "")
   total_score = 0
   # คำนวณผลรวม
   for char in clean_name:
       total_score += char_weights.get(char, 0)
   # เกณฑ์การวิเคราะห์ระดับความหมาย (ตัวอย่างเกณฑ์ทั่วไป)
   # หมายเหตุ: ในทางโหราศาสตร์ เลขแต่ละตัวมีคำทำนายเฉพาะ
   # ในที่นี้แบ่งกลุ่มตามระดับคุณภาพเบื้องต้น
   very_good = [9, 14, 15, 19, 23, 24, 36, 41, 42, 45, 50, 51, 54, 55, 56, 59, 63, 64, 65]
   good = [10, 13, 16, 18, 20, 22, 25, 26, 28, 31, 32, 35, 38, 39, 40, 44, 46, 47, 49, 52, 53, 57, 58, 60, 61, 62]
   if total_score in very_good:
       result = "ดีมาก (Very Good)"
   elif total_score in good:
       result = "ดี (Good)"
   else:
       result = "ให้โทษ (Harmful)"
   return total_score, result
# กำหนดชื่อนักศึกษา
student_name = "สุทธิชัย แก้วลาย"
# ประมวลผล
score, grade = analyze_name(student_name)
# แสดงผลลัพธ์
print("-" * 30)
print(f"ชื่อนักศึกษา: {student_name}")
print(f"ผลรวมเลขศาสตร์: {score}")
print(f"ระดับคุณภาพ: {grade}")
print("-" * 30)


